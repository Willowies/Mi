package com.mi.model.bean;

public class ReceivingPoint {//县级受理点实体类

	
	private int rpId;
	private String rpName;
	private String rpCity;
	private String rpCountry;
	private String rpAddress;
	private String rpTelephone;
	public int getRpId() {
		return rpId;
	}
	public void setRpId(int rpId) {
		this.rpId = rpId;
	}
	public String getRpName() {
		return rpName;
	}
	public void setRpName(String rpName) {
		this.rpName = rpName;
	}
	public String getRpCity() {
		return rpCity;
	}
	public void setRpCity(String rpCity) {
		this.rpCity = rpCity;
	}
	public String getRpCountry() {
		return rpCountry;
	}
	public void setRpCountry(String rpCountry) {
		this.rpCountry = rpCountry;
	}
	public String getRpAddress() {
		return rpAddress;
	}
	public void setRpAddress(String rpAddress) {
		this.rpAddress = rpAddress;
	}
	public String getRpTelephone() {
		return rpTelephone;
	}
	public void setRpTelephone(String rpTelephone) {
		this.rpTelephone = rpTelephone;
	}
	
	
}